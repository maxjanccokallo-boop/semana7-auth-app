// routes/auth.routes.js

// ========================================
// IMPORTS
// ========================================

import express from "express";

import {
  signup,
  signin,
  refreshToken,
  signout
} from "../controllers/auth.controller.js";

import {
  checkDuplicateUsernameOrEmail,
  checkRolesExisted
} from "../middlewares/verifySignUp.js";

import {
  verifyToken
} from "../middlewares/authJwt.js";

// ========================================
// ROUTER
// ========================================

const router = express.Router();

// ========================================
// AUTH ROUTES
// ========================================

/*
|--------------------------------------------------------------------------
| REGISTER
|--------------------------------------------------------------------------
| POST /api/auth/signup
|--------------------------------------------------------------------------
| Crea un nuevo usuario
|--------------------------------------------------------------------------
*/

router.post(
  "/signup",
  [
    checkDuplicateUsernameOrEmail,
    checkRolesExisted
  ],
  signup
);

/*
|--------------------------------------------------------------------------
| LOGIN
|--------------------------------------------------------------------------
| POST /api/auth/signin
|--------------------------------------------------------------------------
| Inicia sesión y genera:
| - accessToken
| - refreshToken
|--------------------------------------------------------------------------
*/

router.post(
  "/signin",
  signin
);

/*
|--------------------------------------------------------------------------
| REFRESH TOKEN
|--------------------------------------------------------------------------
| POST /api/auth/refreshtoken
|--------------------------------------------------------------------------
| Genera un nuevo accessToken
| usando refreshToken
|--------------------------------------------------------------------------
*/

router.post(
  "/refreshtoken",
  refreshToken
);

/*
|--------------------------------------------------------------------------
| SIGNOUT
|--------------------------------------------------------------------------
| POST /api/auth/signout
|--------------------------------------------------------------------------
| Elimina el refresh token
| de la base de datos
|--------------------------------------------------------------------------
*/

router.post(
  "/signout",
  [verifyToken],
  signout
);

// ========================================
// EXPORT
// ========================================

export default router;